<?PHP

  namespace                     YaOO                                {

//  --------------------------- ----------------------------------- --------------------------------
    function                    accessDenied                        ()                              {
        if ( !defined( 'ABSPATH' ) ) { include('index.php'); exit; }
    };
//  --------------------------- ----------------------------------- --------------------------------
    function                    alert                               ($msg='')                       {
        echo "<script>alert('". $msg . "');</script>";
    };
//  --------------------------- ----------------------------------- --------------------------------
    function                    noHotLink                           ()                              {   // To.DO    avoid using REFERER !!
        if (empty($_SERVER['HTTP_REFERER'])) {
            echo 'no hot link !! (please)';
            die;
        }
    }
//  --------------------------- ----------------------------------- --------------------------------
    function                    makeJs                              ()                              {
        header("Content-type: text/javascript");
    }
//  --------------------------- ----------------------------------- --------------------------------
    function                    makeCss                             ()                              {
        header("Content-type: text/css");
    }
//  --------------------------- ----------------------------------- --------------------------------
    function                    noCached                            ()                              {
        header("Cache-Control: no-cache, no-store, max-age=7");
    }
//  --------------------------- ----------------------------------- --------------------------------
    function                    makeNoCacheJs                       ()                              {
        makeJs();
        noCached();
    }
//  --------------------------- ----------------------------------- --------------------------------
    function                    makeNoCacheCss                      ()                              {
        makeCss();
        noCached();
    }
//  --------------------------- ----------------------------------- --------------------------------
    function                    jsRegEx                             ($re)                           {   // fix php regEx so that, can be used in javascript
        return str_replace ('\&','&',$re);
    }
//  --------------------------- ----------------------------------- --------------------------------

  };
